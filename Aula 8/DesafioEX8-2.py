dado = 10
print(f"Conteúdo da variável dado: {dado}")

dado += 1 # dado = dado + 1
print(f"Conteúdo da variável dado, depois do incremento: {dado}")

dado -= 1 # dado = dado - 1
print(f"Conteúdo da variável dado, depois do decremento: {dado}")

dado *= 2 # dado = dado * 2
print(f"Conteúdo da variável dado, depois da multiplicação: {dado}")

dado /= 2 # dado = dado / 2
print(f"Conteúdo da variável dado, depois da divisão: {dado}")

dado %= 2 # dado = dado % 2
print(f"Conteúdo do resto de divisão intera da variável dado, depois da divisão: {dado}")