i = 10                       # i é uma variável de número inteiro

while i >= 0:               # while se trata de uma LAÇO/LOOP tipo ENQUANTO...
    print(i, " ", end="")    # ele pega a Variável i, imprime o valor original e o termo end é pra não pular linha
    i -= 1                   # aqui ele subtrai um na variável e retorna ao While para validar a condição