i = 1                        # i é uma variável de número inteiro

while i <= 10:               # while se trata de uma LAÇO/LOOP tipo ENQUANTO...
    print(i, " ", end="")    # ele pega a Variável i, imprime o valor original e o termo end é pra não pular linha
    i += 1                   # aqui ele soma um na variável e retorna ao While para validar a condição

# o While vai testar a condição e fazer o LOOP enquanto o valor não ultrapassar o número inteiro 10
# a linha 1 é a variável e a 5 é a atuliazação de variável...